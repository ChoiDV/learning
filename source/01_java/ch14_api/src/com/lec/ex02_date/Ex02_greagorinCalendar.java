package com.lec.ex02_date;

import java.util.Calendar;
import java.util.GregorianCalendar;

public class Ex02_greagorinCalendar {
	public static void main(String[] args) {
//		Calendar cal = Calendar.getInstance();
//		System.out.println(cal); // 가독성 저하
		GregorianCalendar cal = new GregorianCalendar();

		int year = cal.get(Calendar.YEAR); // 연도 가져올때
		int month = cal.get(Calendar.MONTH) + 1; // 월 가져오기 (Calendar.MONTH); 는 0,1,2,3,...11월 시스템은 0부터 시작 그래서 +1 해줘야함
		int day = cal.get(Calendar.DAY_OF_MONTH); // 일
		int week = cal.get(Calendar.DAY_OF_WEEK); // 요일  일요일 1,월2,화3,수4, ... 토 7 을 반환
		int hour24 = cal.get(Calendar.HOUR_OF_DAY); // 24시간 단위 
		int ampm = cal.get(Calendar.AM_PM);  //  오전(0)/오후(1) 
		int hour = cal.get(Calendar.HOUR);   // 12시간 단위 
		int minute = cal.get(Calendar.MINUTE);  // 분
		int second = cal.get(Calendar.SECOND); // 초
		int millisec = cal.get(Calendar.MILLISECOND); // 초 다음(뒷) 단위 1000이 1초가 됨 
		
		System.out.printf("%d년%d월%d일", year, month, day);
		
		switch (week) { // week 요일로 바꾸기 
		case 1:
			System.out.println("일요일");
			break;
		case 2:
			System.out.println("월요일");
			break;
		case 3:
			System.out.println("화요일");
			break;
		case 4:
			System.out.println("수요일");
			break;
		case 5:
			System.out.println("목요일");
			break;
		case 6:
			System.out.println("금요일");
			break;
		case 7:
			System.out.println("토요일");
			break;
		}
		
		System.out.print(ampm == 0 ? "오전" : "오후");
		System.out.printf("%d시 %d분 %d초 %d",hour,minute,second,millisec);
		System.out.println();
		// %d(정수) %f(실수) %s(문자열) %c(문자) 
		// %tY(년) %tm(월) %td(일) %ta(요일) %tH(24시) %tl(12시) %tM(분) %tS(초)
		System.out.printf("%tY년 %tm월 %td일 %ta요일 %tp %tl시 %tM분 %tS초", cal,cal,cal,cal,cal,cal,cal,cal);
		System.out.println();
		System.out.printf("%1$tY년 %1$tm월 %1$td일 %1$ta요일 %1$tp %1$tl시 %1$tM분 %1$tS초", cal);
		
		
		

	}
}
